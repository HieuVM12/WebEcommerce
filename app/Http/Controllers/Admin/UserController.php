    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $user=User::findOrFail($id);
        if($user->role_as == 1 && $user->id != Auth::user()->id){
            return abort(404);
        }
        return view('admin.user.edit',compact('user'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $user = User::findOrFail($id);
        if($user->role_as == 1 && $user->id != Auth::user()->id){
            return abort(404);
        }
        $user->update([
            'name' => $request->name,
            'email' => $request->email,
            'role_as' => $request->role_as==true?'1':'0'
        ]);
        return redirect('admin/users/')->with('message','Cập nhật thành công');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $user = User::findOrFail($id);
        if($user->role_as == 1 && $user->id != Auth::user()->id){
            return redirect()->back()->with('message','Không thể xóa tài khoản admin khác');
        }
        $user->delete();
        return redirect()->back()->with('message','Xóa tài khoản thành công');
    }
}
